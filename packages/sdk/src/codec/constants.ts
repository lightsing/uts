import { type DigestOp, type Op } from '../types.ts'

export const OP_CODE_MAP: Record<Op, number> = {
  SHA1: 0x02,
  RIPEMD160: 0x03,
  SHA256: 0x08,
  KECCAK256: 0x67,
  APPEND: 0xf0,
  PREPEND: 0xf1,
  REVERSE: 0xf2,
  HEXLIFY: 0xf3,
  ATTESTATION: 0x00,
  FORK: 0xff,
}

export const DIGEST_LENGTHS: Record<DigestOp, number> = {
  SHA1: 20,
  RIPEMD160: 20,
  SHA256: 32,
  KECCAK256: 32,
}

export const getOpName = (code: number): Op => {
  return Object.entries(OP_CODE_MAP).find(([_, v]) => v === code)?.[0] as Op
}

